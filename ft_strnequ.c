#include "libft.h"

int		ft_strnequ(const char *s1, const char *s2, size_t n)
{
	size_t i;

	i = 0;
	while (i < n && s1[i] && s1[i] == s2[i])
		i++;
	if (i == n || (s1[i] == s2[i] && s1[i] == 0))
		return (1);
	else
		return (0);
}
